package aulasJavaPOO.arrays;

public class ForEach {

	public static void main(String[] args) {
		
		int[] numeros = new int[10];
		
		// depois do : é o vetor. 
		for (int numero1: numeros) {
			//em cada vetor ele vai imprimir a variável numero1.
			System.out.println(numero1 + "\t");
		}
		
	}

}
